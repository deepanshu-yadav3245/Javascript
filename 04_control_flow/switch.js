// Switch Statement:----

/**
 * Evaluates a value and executes code based on matching cases
 * @param {string|number} value - The value to evaluate against cases
 * @returns {void}
 * @example
 * switch(day) {
 *   case 1:
 *     console.log("Monday");
 *     break;
 *   case 2:
 *     console.log("Tuesday");
 *     break;
 *   default:
 *     console.log("Other day");
 * }
 */

const day = 3;

switch(day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    default:
        console.log("Weekend");
}


console.log("============================================================")

